package com.example.springboot_resttemplate_demo.model;

/**
 * @author licunzhi
 * @desc 描述功能
 * @date 2018-10-29
 */
public class User {

    private String id;
    private String name;
    private String email;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
